ALGO_INFO_MD = """
**K‑Means**  
Центроидный алгоритм: делит данные на *k* кластеров, минимизируя внутрикластерную  
сумму квадратов расстояний. Быстрый и простой, но чувствителен к масштабированию  
и плохо работает с вытянутыми/перекрывающимися кластерами и выбросами.

**DBSCAN**  
Density‑Based Spatial Clustering of Applications with Noise. Объединяет точки,  
у которых достаточно плотные соседи (параметры *eps*, *min_samples*), а разреженные  
объявляет «шумом». Находит кластеры произвольной формы и не требует задавать *k*.

**Agglomerative Clustering**  
Иерархическая «снизу‑вверх» кластеризация: каждая точка стартует отдельным кластером,  
далее пары кластеров итеративно объединяются по выбранному критерию связи  
(average, complete, ward…). Итог можно визуализировать дендрограммой.

**Gaussian Mixture (EM‑GMM)**  
Модель смеси *k* многомерных нормальных распределений; обучается алгоритмом EM.  
Даёт «мягкое» (вероятностное) присвоение точек компонентам, умеет описывать  
овоидные (эллиптические) кластеры с различными ковариационными матрицами.

**Spectral Clustering**  
Строит граф близостей, вычисляет несколько собственных векторов матрицы Лапласа  
(«спектр») и кластеризует их (обычно K‑Means). Хорошо отделяет кластеры  
не‑сферической формы, но вычислительно затратен на больших выборках.

**Mean‑Shift**  
Метод поисков моды ядровой плотности: точки сдвигаются к ближайшему локальному  
максимуму плотности, образуя кластеры вокруг «мод». Не требует заранее *k*,  
обнаруживает произвольные формы, но медленный в высоких размерностях.
"""
