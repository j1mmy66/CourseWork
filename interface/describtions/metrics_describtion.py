METRIC_INFO_MD = """
**Silhouette Score**  
Диапазон −1 … 1. Сравнивает среднее расстояние объекта до точек своего кластера  
и до ближайшего чужого кластера. Чем ближе к 1, тем кластеры компактнее  
и лучше разделены.

**Davies–Bouldin Index**  
Безразмерный индекс, тем меньше — тем лучше. Считает отношение внутрикластерного  
«размытия» к межкластерной дистанции, усреднённое по всем кластерам.  
Хорошее разбиение даёт маленькие значения (идеально → 0).

**Calinski–Harabasz Index**  
Отношение межкластерной дисперсии к внутрикластерной. Чем индекс выше,  
тем кластеры плотнее внутри и дальше друг от друга. Используется для выбора оптимального *k*.

**Adjusted Rand Index (ARI)**  
Диапазон −1 … 1; 0 ≈ случай, 1 — идеальное совпадение. Смотрит на все пары объектов:  
совпали ли они «внутри/вне» одного кластера относительно истинных меток.  
Корректируется на случайные соглашения.

**Normalized Mutual Information (NMI)**  
Значения 0 … 1. Основана на информационной теории: сколько информации о классах  
заключено в кластерах (и наоборот) после нормализации на энтропию.  
1 означает полное совпадение разбиений.

**Homogeneity**  
Показывает, содержит ли каждый кластер объекты только одного класса.  
0 … 1, где 1 — идеальная однородность. Полезно, когда важна «чистота» кластеров.

**Completeness**  
Измеряет, находятся ли все объекты одного класса в одном кластере.  
0 … 1; 1 — все объекты каждого класса не распылены, а собраны вместе.  
Балансирует *homogeneity*.

**V‑Measure**  
Гармоническое среднее *homogeneity* и *completeness*.  
Значения 0 … 1; 1 — идеал, когда кластеры чисты и классы цельны.  
Удобно как единая сводная оценка.
"""
